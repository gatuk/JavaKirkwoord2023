package en.codegym.task.pro.task09.task0904;

/* 
Character literals
*/

public class Solution {
    public static char first = '\u004A';
    public static char second = 'a';
    public static char third = '\u0076';
    public static char fourth = 'a';

    /*
    1. The first variable needs to be assigned the value '\u004A'.
    2. The second variable needs to be assigned the value 'a'.
    3. The third variable needs to be assigned the value '\u0076'.
    4. The fourth variable needs to be assigned the value 'a'.
     */

    public static void main(String[] args) {
        System.out.print(first);
        System.out.print(second);
        System.out.print(third);
        System.out.print(fourth);
    }
}
